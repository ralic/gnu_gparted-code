<h1>How to help</h1>
<h3>Bugs</h3>
<p>
Properly filed bugs are a great help while developing software.<br>
The bugs page can be found <a href="http://gparted.sourceforge.net/bugs.php">here</a>.
</p>
<h3>Forum</h3>
<p>
The forums are the first place to ask and receive support. It would be great if users would help each other.<br>
This is also the place to ask for and discuss new features.<br>
Sign up and join the fun <a href="http://gparted.sourceforge.net/forum.php">here</a>.
</p>
<h3>Language Translation</h3>
<p>
Translating GParted to your language is a valuable task that enables even more people to comfortably use GParted.<br>
Learn more about the GNOME Translation Project <a href="http://live.gnome.org/TranslationProject/">here</a>.<br>
View the status of GParted language translation <a href="http://l10n.gnome.org/module/gparted/">here</a>.
</p>
<h3>Testing</h3>
<p>
Testing the functionality of GParted,
reporting <a href="bugs.php">bugs</a>, and discussing problems is a
huge help to developing robust software.<br>  Discussions on testing
take place in
the <a href="http://gparted-forum.surf4.info/viewforum.php?id=8">Testers</a>
forum.
</p>
<h3>Coding</h3>
<p>
Fixing <a href="bugs.php">bugs</a>, implementing <a href="http://gparted-forum.surf4.info/viewforum.php?id=5">new features</a>,
or addressing 'FIXME' and 'TODO' comments in the code can be great
contributions to the GParted project.<br>
<br>
To minimize the chance of duplicate efforts, please
contact <a href="mailto:gedakc&#064;users.sf.net">Curtis Gedak</a>
before embarking on significant code changes.<br>
<br>
See also <a href="dev-status.php">development status</a>.
</p>
<h3>Donations</h3>
<p>
Another big way to help us to make GParted better is to make a
donation to the GParted project.  For details on donations please
refer to our
<a href="http://gparted.sourceforge.net/donations.php">donations page</a>.
</p>

